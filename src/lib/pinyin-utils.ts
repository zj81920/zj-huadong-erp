/**
 * 获取单个汉字的拼音首字母，非汉字返回原字符大写
 */
function getCharPinyin(ch: string): string {
  return CHAR_PINYIN_MAP[ch] || ch.toUpperCase();
}

/**
 * 获取姓名的拼音首字母缩写
 * 例如 "张三" → "ZS", "李四" → "LS"
 */
function getPinyinAbbr(name: string): string {
  let result = "";
  for (const ch of name) {
    if (/[\u4e00-\u9fff]/.test(ch)) {
      result += getCharPinyin(ch);
    }
  }
  return result;
}

/**
 * 拼音模糊匹配
 * @param input 用户输入的搜索字符串
 * @param name 要匹配的姓名
 * @returns 是否匹配
 *
 * 匹配规则：
 * 1. 直接子串匹配（大小写不敏感）：input 是 name 的子串
 * 2. 拼音首字母匹配：input 匹配姓名的拼音首字母缩写
 *
 * 示例：
 * - pinyinMatch("张", "张三") → true
 * - pinyinMatch("zs", "张三") → true
 * - pinyinMatch("z", "张三") → true
 */
export function pinyinMatch(input: string, name: string): boolean {
  if (!input || !name) return false;
  const q = input.toLowerCase().trim();
  if (!q) return false;

  if (name.toLowerCase().includes(q)) return true;

  const abbr = getPinyinAbbr(name).toLowerCase();
  if (abbr.includes(q)) return true;

  return false;
}
